def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Rewrite seed_v2 JSONL with responses generated by a local vLLM OpenAI-compatible model."
    )
    parser.add_argument("--input", required=True, help="Existing seed_v2 JSONL path.")
    parser.add_argument("--doc", required=True, help="Source markdown document path.")
    parser.add_argument("--output", required=True, help="Output JSONL path.")
    parser.add_argument(
        "--api-base",
        default="http://127.0.0.1:8000/v1",
        help="OpenAI-compatible base URL.",
    )
    parser.add_argument(
        "--model",
        default="gpt-oss-20b-seed-ch01-ch02",
        help="Served model ID exposed by vLLM.",
    )
    parser.add_argument(
        "--max-retries",
        type=int,
        default=3,
        help="Retry count per API call.",
    )
    parser.add_argument(
        "--start-id",
        help="If provided, rewrite starting from this record id.",
    )
    return parser.parse_args()


def read_jsonl(path: Path) -> list[dict[str, Any]]:
    records: list[dict[str, Any]] = []
    for line in path.read_text(encoding="utf-8").splitlines():
        line = line.strip()
        if not line:
            continue
        records.append(json.loads(line))
    return records


def write_jsonl(path: Path, records: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        for record in records:
            f.write(json.dumps(record, ensure_ascii=False))
            f.write("\n")


def clean_heading(text: str) -> str:
    return re.sub(r"\s+", " ", text.strip())


def parse_sections(text: str) -> dict[tuple[str, ...], str]:
    lines = text.splitlines()
    headings: list[tuple[int, int, str]] = []
    for i, line in enumerate(lines):
        match = re.match(r"^(#{1,6})\s+(.*)$", line)
        if match:
            headings.append((i, len(match.group(1)), clean_heading(match.group(2))))

    section_map: dict[tuple[str, ...], str] = {}
    stack: list[tuple[int, str]] = []

    for idx, (line_no, level, title) in enumerate(headings):
        while stack and stack[-1][0] >= level:
            stack.pop()
        stack.append((level, title))
        path_titles = tuple(item[1] for item in stack)
        end = headings[idx + 1][0] if idx + 1 < len(headings) else len(lines)
        body = "\n".join(lines[line_no + 1 : end]).strip()
        section_map[path_titles] = body
    return section_map


def extract_table_blocks(text: str) -> list[str]:
    blocks: list[str] = []
    current: list[str] = []
    for line in text.splitlines():
        if line.strip().startswith("|"):
            current.append(line.rstrip())
        else:
            if current:
                blocks.append("\n".join(current))
                current = []
    if current:
        blocks.append("\n".join(current))
    return blocks


def split_section_path(section_path: str) -> tuple[str, ...]:
    return tuple(part for part in section_path.split(">") if part and not part.startswith("table"))


def extract_table_index(section_path: str) -> int | None:
    for part in section_path.split(">"):
        match = re.fullmatch(r"table(\d+)", part)
        if match:
            return int(match.group(1))
    return None


def build_group_context(section_map: dict[tuple[str, ...], str], section_path: str) -> str:
    base_path = split_section_path(section_path)
    body = section_map.get(base_path, "")
    table_index = extract_table_index(section_path)
    if table_index is None:
        return body
    blocks = extract_table_blocks(body)
    if 1 <= table_index <= len(blocks):
        return body + "\n\n[표 본문]\n" + blocks[table_index - 1]
    return body


def shrink_context_for_prompt(context: str, qa_type: str) -> str:
    if qa_type != "table_explainer":
        return context
    lines = context.splitlines()
    if len(lines) <= 18:
        return context
    return "\n".join(lines[:18]) + "\n[표의 나머지 행은 동일한 구조로 이어짐]"


def call_chat(api_base: str, model: str, messages: list[dict[str, str]], max_retries: int) -> str:
    payload = {
        "model": model,
        "messages": messages,
        "temperature": 0.0,
        "seed": 42,
        "max_tokens": 1800,
    }
    data = json.dumps(payload, ensure_ascii=False).encode("utf-8")
    req = urllib.request.Request(
        url=f"{api_base}/chat/completions",
        data=data,
        headers={"Content-Type": "application/json", "Authorization": "Bearer dummy"},
        method="POST",
    )
    last_error: Exception | None = None
    for attempt in range(max_retries):
        try:
            with urllib.request.urlopen(req, timeout=180) as resp:
                body = json.loads(resp.read().decode("utf-8"))
                message = body["choices"][0]["message"]
                content = message.get("content")
                if isinstance(content, list):
                    text_parts: list[str] = []
                    for item in content:
                        if isinstance(item, dict) and item.get("type") == "text":
                            text_parts.append(item.get("text", ""))
                    content = "".join(text_parts)
                if content is None:
                    content = message.get("reasoning_content") or message.get("reasoning")
                if content is None:
                    raise ValueError(f"chat completion has no usable content: {body}")
                return content
        except Exception as exc:  # noqa: BLE001
            last_error = exc
            time.sleep(2 * (attempt + 1))
    raise RuntimeError(f"chat completion failed: {last_error!r}")


def extract_json(text: str) -> dict[str, Any]:
    match = re.search(r"\{.*\}", text, flags=re.DOTALL)
    if not match:
        raise ValueError("model response does not contain JSON object")
    return json.loads(match.group(0))


def parse_structured_response(text: str, expected_id: str) -> dict[str, Any]:
    try:
        parsed = extract_json(text)
        records = parsed.get("records", [])
        item = next((candidate for candidate in records if candidate.get("id") == expected_id), None)
        if item is None and len(records) == 1:
            item = records[0]
        if item is not None:
            return item
    except Exception:
        pass

    def extract_block(label: str) -> str:
        match = re.search(
            rf"{label}:\s*(.*?)\s*(?=(?:ID|USER|QUESTION|ASSISTANT|ANSWER|KEYPOINTS):|$)",
            text,
            flags=re.DOTALL,
        )
        return match.group(1).strip() if match else ""

    keypoints_block = extract_block("KEYPOINTS")
    keypoints: list[str] = []
    for line in keypoints_block.splitlines():
        line = line.strip()
        if not line:
            continue
        keypoints.append(line.lstrip("- ").strip())

    item = {
        "id": extract_block("ID") or expected_id,
        "user": extract_block("USER") or extract_block("QUESTION"),
        "assistant": extract_block("ASSISTANT") or extract_block("ANSWER"),
        "answer_key_points": keypoints,
    }
    if not item["assistant"]:
        raise ValueError("model response does not contain parsable structured fields")
    return item


def make_prompt(group_path: str, context: str, records: list[dict[str, Any]]) -> list[dict[str, str]]:
    record = records[0]
    qa_type = record["meta"]["qa_type"]

    extra_rules: list[str] = [
        "- 답변은 너무 길게 쓰지 말고 보통 3~6개 bullet 안으로 정리할 것",
        "- 문서에 없는 내용은 추가하지 않되, 의미 연결을 위한 최소한의 설명만 허용할 것",
        "- 이 최소 설명은 용어 풀이와 생략된 주어/관계 보완 수준에 한할 것",
        "- 답변은 자연스러운 문장으로 연결하되, 새로운 정보 추가 없이 기존 내용을 매끄럽게 표현할 것",
        "- 새로운 판단 기준, 규칙, 예외, 질문에 없는 대상이나 비교는 추가하지 말 것",
    ]
    if qa_type == "table_explainer":
        extra_rules.extend(
            [
                "- 표의 모든 행을 다 나열하지 말 것",
                "- 열 의미를 설명하고 대표적인 구분 예시만 3~5개 들 것",
                "- 답변 전체는 최대 5개 bullet, 최대 7줄 이내로 작성할 것",
                "- 마지막에는 `나머지 항목도 같은 기준으로 확장된다`처럼 압축 요약할 수 있음",
            ]
        )
    if qa_type == "coverage":
        extra_rules.extend(
            [
                "- coverage 답변은 하위 제목 전체를 포괄하되, 하위 제목별 핵심만 한 줄씩 요약할 것",
                "- coverage 답변도 최대 5개 bullet 안으로 작성할 것",
            ]
        )
    if qa_type in {"concept", "definition", "rule"}:
        extra_rules.extend(
            [
                "- 답변은 핵심 정의/기준/예외를 남기되, 의미 연결에 필요한 최소 설명만 덧붙일 것",
                "- 원문을 길게 복사하지 말 것",
                "- 문서에 없는 새로운 판단 기준, 규칙, 예외는 추가하지 말 것",
                "- 예시는 꼭 필요할 때만 최대 2개까지만 들 것",
                "- 표가 있더라도 모든 항목을 나열하지 말고 구조만 설명할 것",
            ]
        )
    if qa_type == "definition":
        extra_rules.extend(
            [
                "- definition 답변에는 정의문이 반드시 포함되어야 할 것",
                "- 질문이 직접 묻지 않으면 절차, 비교, 예외를 덧붙이지 말 것",
            ]
        )
    if qa_type == "rule":
        extra_rules.extend(
            [
                "- rule 답변은 판단 기준이나 규칙만 제시하고 적용 절차 설명으로 확장하지 말 것",
            ]
        )

    user_prompt = f"""
아래 문서 조각을 근거로 학습용 질문/답변 1건을 자연스럽게 다시 작성해줘.

중요 규칙:
- 질문은 실제 사용자가 물을 법한 자연스러운 한국어로 작성할 것
- 답변은 간결하지만 정확하게 작성할 것
- `coverage`는 하위 제목 전체를 포괄하는 요약형 질문/답변으로 만들 것
- `table_explainer`는 표의 기준, 구분값, 행/열 의미가 드러나게 만들 것
- 기존 draft는 품질이 낮으므로 참고만 하고 그대로 베끼지 말 것
- 아래 추가 길이/요약 규칙을 반드시 지킬 것
{chr(10).join(extra_rules)}
- 아래 형식만 반환할 것
- 다른 설명, 사고과정, 서론, 코드블록을 절대 쓰지 말 것
- JSON을 쓰지 말고, 아래 텍스트 형식으로만 반환할 것

반환 형식(반드시 그대로):
ID: 원본 id
QUESTION: 새 질문
ANSWER:
새 답변
KEYPOINTS:
- 핵심 포인트1
- 핵심 포인트2

문서 경로/구간:
{group_path}

문서 본문:
\"\"\"
{context}
\"\"\"

재작성 대상:
- ID: {record["id"]}
- QA 유형: {qa_type}
- 섹션 제목: {record["meta"]["section_title"]}
- 기존 질문 초안: {record["messages"][1]["content"]}

위 형식 그대로만 출력해.
""".strip()

    return [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": user_prompt},
    ]


def rewrite_records(
    records: list[dict[str, Any]],
    section_map: dict[tuple[str, ...], str],
    api_base: str,
    model: str,
    max_retries: int,
    start_id: str | None = None,
    save_path: Path | None = None,
) -> list[dict[str, Any]]:
    rewritten: dict[str, dict[str, Any]] = {}
    started = start_id is None
    for record in records:
        if not started:
            if record["id"] == start_id:
                started = True
            else:
                rewritten[record["id"]] = record
                continue
        section_path = record["meta"]["section_path"]
        context = build_group_context(section_map, section_path)
        context = shrink_context_for_prompt(context, record["meta"]["qa_type"])
        item: dict[str, Any] | None = None
        last_error: Exception | None = None
        for _ in range(max_retries):
            try:
                single_messages = make_prompt(section_path, context, [record])
                single_raw = call_chat(api_base, model, single_messages, max_retries)
                item = parse_structured_response(single_raw, record["id"])
                if item is not None:
                    break
                raise ValueError(f"missing rewritten item for {record['id']}")
            except Exception as exc:  # noqa: BLE001
                last_error = exc
                time.sleep(1)
        if item is None:
            raise RuntimeError(f"failed to rewrite {record['id']}: {last_error!r}")

        user_text = item.get("user") or item.get("question") or record["messages"][1]["content"]
        assistant_text = item.get("assistant") or item.get("answer") or item.get("response")
        if not assistant_text and isinstance(user_text, str):
            stripped = user_text.strip()
            if stripped.startswith("-") or "\n-" in stripped or "\n1." in stripped:
                assistant_text = stripped
                user_text = record["messages"][1]["content"]
        if not assistant_text:
            raise ValueError(f"missing assistant text for {record['id']}")

        record["messages"][0]["content"] = SYSTEM_PROMPT
        record["messages"][1]["content"] = str(user_text).strip()
        record["messages"][2]["content"] = str(assistant_text).strip()
        key_points = item.get("answer_key_points") or record["meta"]["answer_key_points"]
        if isinstance(key_points, list) and key_points:
            record["meta"]["answer_key_points"] = [str(x).strip() for x in key_points if str(x).strip()]
        rewritten[record["id"]] = record
        if save_path is not None:
            ordered_records = [rewritten.get(existing["id"], existing) for existing in records]
            write_jsonl(save_path, ordered_records)
        print(f"rewritten: {record['id']}")

    return [rewritten[record["id"]] for record in records]


def main() -> None:
    args = parse_args()
    input_path = Path(args.input).resolve()
    doc_path = Path(args.doc).resolve()
    output_path = Path(args.output).resolve()

    records = read_jsonl(input_path)
    markdown = doc_path.read_text(encoding="utf-8")
    section_map = parse_sections(markdown)
    rewritten = rewrite_records(
        records,
        section_map,
        args.api_base,
        args.model,
        args.max_retries,
        args.start_id,
        output_path,
    )
    write_jsonl(output_path, rewritten)
    print(f"wrote {len(rewritten)} records to {output_path}")


if __name__ == "__main__":
    main()
