    "답변은 자연스러운 문장으로 연결하되, 새로운 정보 추가 없이 기존 내용을 매끄럽게 표현합니다. "
    "새로운 판단 기준, 규칙, 질문에 없는 대상이나 비교는 추가하지 않습니다."
